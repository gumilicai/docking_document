package com.gumilicai.platform.entity;

public class Assets {
	private Float awaitAmount;
	private Float balanceAmount;
	private Float totalAmount;
	private Float frozenAmount;
	private Float currentAmount;

	public Float getAwaitAmount() {
		return awaitAmount;
	}

	public void setAwaitAmount(Float awaitAmount) {
		this.awaitAmount = awaitAmount;
	}

	public Float getBalanceAmount() {
		return balanceAmount;
	}

	public void setBalanceAmount(Float balanceAmount) {
		this.balanceAmount = balanceAmount;
	}

	public Float getTotalAmount() {
		return totalAmount;
	}

	public void setTotalAmount(Float totalAmount) {
		this.totalAmount = totalAmount;
	}

	public Float getFrozenAmount() {
		return frozenAmount;
	}

	public void setFrozenAmount(Float frozenAmount) {
		this.frozenAmount = frozenAmount;
	}

	public Float getCurrentAmount() {
		return currentAmount;
	}

	public void setCurrentAmount(Float currentAmount) {
		this.currentAmount = currentAmount;
	}
}
